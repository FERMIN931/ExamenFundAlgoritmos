# Leer la cantidad de productos
N = int(input("Ingrese el n√∫mero de productos: "))
subtotal = 0

# Calcular el subtotal
for i in range(1, N + 1):
    precio = float(input(f"Ingrese el precio del producto {i}: "))
    subtotal += precio

# Calcular IGV y total
IGV = subtotal * 0.18
total = subtotal + IGV

# Mostrar resultados
print(f"Subtotal: {subtotal}")
print(f"IGV (18%): {IGV}")
print(f"Total a pagar: {total}")